# 슬롯 인덱스 → 갑자 (0-indexed: slot 0 = 01h1m)
def _slot_index(hour: int, minute: int) -> int:
    return (hour - 1) * 5 + (minute - 1)


def get_gapja(hour: int, minute: int) -> dict:
    """시/분 좌표 → 60갑자 메타데이터"""
    idx = _slot_index(hour, minute)
    hanja, hangul, cheongan, jiji, ohaeng, phase, story = _GAPJA[idx]
    return {
        "index": idx + 1,           # 1~60
        "hanja": hanja,
        "hangul": hangul,
        "cheongan": cheongan,       # 천간
        "jiji": jiji,               # 지지
        "ohaeng": ohaeng,           # 오행 조합
        "phase_code": phase,        # 국면 코드
        "story": story,             # 단키 서사
    }


def get_gapja_by_index(n: int) -> dict:
    """1~60 인덱스 → 갑자 메타데이터"""
    if not 1 <= n <= 60:
        raise ValueError(f"index must be 1~60, got {n}")
    hanja, hangul, cheongan, jiji, ohaeng, phase, story = _GAPJA[n - 1]
    hour = (n - 1) // 5 + 1
    minute = (n - 1) % 5 + 1
    return {
        "index": n,
        "hour": hour,
        "minute": minute,
        "hanja": hanja,
        "hangul": hangul,
        "cheongan": cheongan,
        "jiji": jiji,
        "ohaeng": ohaeng,
        "phase_code": phase,
        "story": story,
    }


